  CUSTOMER_ID NUMBER;
  SUB_ID NUMBER;
  BILL_MONTH VARCHAR2(20);
BEGIN
  CUSTOMER_ID := 10002;
  SUB_ID := 1002;
  BILL_MONTH := '2022-JAN-20';

  GENERATE_BILL(
    CUSTOMER_ID => CUSTOMER_ID,
    SUB_ID => SUB_ID,
    BILL_MONTH => BILL_MONTH
  );
