create or replace procedure emp_details as
V_ENAME VARCHAR(25);
V_SAL NUMBER(25);
begin
select ename,sal INTO V_ENAME,V_SAL from emp;
DBMS_OUTPUT.PUT_LINE (V_ENAME || 'V_NAME');
DBMS_OUTPUT.PUT_LINE (V_SAL || 'V_SAL');
end;

